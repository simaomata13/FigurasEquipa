public class Ponto {
    private float pontoX;
    private float pontoY;

    public Ponto(float pontoX, float pontoY) {
        this.pontoX = pontoX;
        this.pontoY = pontoY;
    }

    public float getPontoX() {
        return pontoX;
    }

    public void setPontoX(float pontoX) {
        this.pontoX = pontoX;
    }

    public float getPontoY() {
        return pontoY;
    }

    public void setPontoY(float pontoY) {
        this.pontoY = pontoY;
    }
}
